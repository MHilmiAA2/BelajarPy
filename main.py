# Matematika
a = int(input("Masukkan nilai a : "))
b = int(input("Masukkan nilai b : "))
print(a + b)   # 13
print(a - b)   # 7
print(a * b)   # 30
print(a / b)   # 3.333...
print(a % b)   # 1 (sisa bagi)
print(a ** b)  # 1000 (pangkat)

# Perbandingan
print(a > b)   # True
print(a == b)  # False

# Logika
x = True
y = False
print(x and y) # False
print(x or y)  # True
